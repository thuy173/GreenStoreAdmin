import { getApi, putApi2, postApi, deleteApi } from './agent';

const PlanServices = {
  getAll: async () => {
    try {
      const result = await getApi(`plan`, '');
      return result;
    } catch (error) {
      console.log(error);
    }
    return null;
  },

  getById: async (id) => {
    try {
      const result = await getApi(`plan/${id}`, '');
      return result;
    } catch (error) {
      console.log(error);
    }
    return null;
  },

  addData: async (payload) => {
    try {
      const result = await postApi('plan', payload);
      return result;
    } catch (error) {
      console.log(error);
    }
    return null;
  },
  editData: async (id, payload) => {
    try {
      const result = await putApi2(`plan/${id}`, payload);
      return result;
    } catch (error) {
      console.log(error);
    }
    return null;
  },

  deleteData: async (id) => {
    try {
      const result = await deleteApi(`plan/${id}`);
      return result;
    } catch (error) {
      console.log(error);
    }
    return null;
  },
};

export default PlanServices;
